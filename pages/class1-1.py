print("Hello World")  # print是在終端機顯示文字的指令
# 基本型態
print(1)  # int
print(1.0)  # float
print(1.234)  # float
print("apple")  # str
print(True)  # bool
print(False)  # bool

print(1 + 1)  # 2
print("1" + "1")  # 11，字串加法是字串拼接

# 變數
a = 10  # 新增一個儲存空間並取名為a
# "="的功能是將右邊的值存入左邊的變數
print(a)  # 在終端機顯示a的值
a = "apple"  # 將a的值變成"apple"
print(a)  # 在終端機顯示a的值
print(1 + 1)  # 加法後的值
print(1 - 1)  # 減法後的值
print(1 * 1)  # 乘法後的值
print(1 / 1)  # 除法後的值
print(1 // 1)  # 整數除法後的值
print(1**1)  # 次方後的值
print(1 % 1)  # 取餘數後的值
# 優先順序
# 1. ( ) 誇號
# 2. 乘除次方
# 3. 加減
# 4. 比較
# 5. 邏輯
# 6. 位元運算

# 字串
s = "apple"  # 字串
s = "apple"  # 重複宣告
